#include <stdio.h>

/**
 * main - Entry point
 *
 * This program prints single-digit numbers in base 10 without using char variables.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
    /* Print numbers 0 to 9 using putchar */
    putchar('0');
    putchar('1');
    putchar('2');
    putchar('3');
    putchar('4');
    putchar('5');
    putchar('6');
    putchar('7');
    putchar('8');
    putchar('9');

    /* Print a newline character */
    putchar('\n');

    return (0);
}
